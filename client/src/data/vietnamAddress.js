// Hàm lấy danh sách quận/huyện theo tỉnh
export const getDistricts = (city) => {
  if (!city || !vietnamAddressData[city]) return [];
  return Object.keys(vietnamAddressData[city]);
};

// Hàm lấy danh sách phường/xã theo quận/huyện
export const getWards = (city, district) => {
  if (
    !city ||
    !district ||
    !vietnamAddressData[city] ||
    !vietnamAddressData[city][district]
  )
    return [];
  return vietnamAddressData[city][district];
};
